package domain;

public class blog_visitor {

	private String visitor_id;
	private String visitor_pwd;
	private String visitor_nickname;
	private String visitor_gender;
	public String getVisitor_id() {
		return visitor_id;
	}
	public void setVisitor_id(String visitor_id) {
		this.visitor_id = visitor_id;
	}
	public String getVisitor_pwd() {
		return visitor_pwd;
	}
	public void setVisitor_pwd(String visitor_pwd) {
		this.visitor_pwd = visitor_pwd;
	}
	public String getVisitor_nickname() {
		return visitor_nickname;
	}
	public void setVisitor_nickname(String visitor_nickname) {
		this.visitor_nickname = visitor_nickname;
	}
	public String getVisitor_gender() {
		return visitor_gender;
	}
	public void setVisitor_gender(String visitor_gender) {
		this.visitor_gender = visitor_gender;
	}
}
